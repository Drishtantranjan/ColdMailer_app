appTextS1(
    String text, {
      Color? color,
      TextAlign? textAlign,
      FontWeight? fontWeight,
      TextDecoration? decoration,
      TextOverflow? overflow,
    }) =>
    Text(
      text,
      textAlign: textAlign,
      style: AppStyles.s1.copyWith(
          color: color,
          fontWeight: fontWeight,
          decoration: decoration,
